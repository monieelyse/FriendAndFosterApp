class Dog {
  final String id;
  final String name;
  final String breed;
  final String imageUrl;
  final int age; // in years
  final String description;
  final String location;

  Dog({
    required this.id,
    required this.name,
    required this.breed,
    required this.imageUrl,
    required this.age,
    required this.description,
    required this.location,
  });
}
